# Task 3: Function with Two Parameters​

# Define a function that takes two numbers and prints their sum.
def addition(num1, num2):
    print(f"The sum of {num1} and {num2} is {num1 + num2}")

addition(5, 10)
addition(6, 14)
addition(8, 7)


